import json

# 프롬프트 관련

class PromptBuilder:

    @staticmethod
    def build_suggestion_prompt(user_ingredients: list) -> str:
        return f"""
    당신은 전문 요리사 AI입니다. 사용자가 보유한 재료를 최대한 활용하여 만들 수 있는 요리 6가지를 추천하세요.

    📌 사용 가능한 재료 목록:
    {json.dumps(user_ingredients, ensure_ascii=False)}

    필수 조건:
    - 반드시 사용자가 가진 재료만 사용하세요. (추가 재료는 절대 포함하지 마세요)
    - 사용자의 보유 재료는 모두 **냉장고 속 식재료**입니다.  
      즉, 소금, 설탕, 간장, 고추장, 식용유, 참기름, 후춧가루, 밥 등 **기본 조미료 및 주방에 흔히 있는 재료**는  
      별도 명시가 없더라도 가지고 있는 것으로 간주하고 사용할 수 있습니다.  
      단, 이 외의 재료는 절대 임의로 포함하지 마세요.

    - 현실적으로 조리 가능한, **창의적이고 수준 높은 요리**를 추천하세요.
    - 요리의 **다양성**을 고려하세요. 동일한 형태(예: 전, 볶음밥, 샐러드 등)만 반복하지 말고,  
      구이, 찜, 토스트, 오븐 요리, 수제 간식 등 **다양한 방식의 요리**를 구성하세요.
    - 대중적으로 인식되는 **실제 요리 이름**을 사용하세요.
      예: 오징어 파스타, 참치 토스트, 간장 돼지구이, 스팸구이덮밥 등

    - **식재료로 사용할 수 없는 항목**(예: 사람, 동물, 똥, 오줌, 플라스틱, 돌, 금속, 종이, 유리 등 비식용 물질)이 포함되어 있다면,  
      요리 추천은 절대 하지 말고 다음과 같은 형식으로 에러 메시지를 정확히 JSON으로 출력하세요:
    {{
      "error": "식재료로 사용할 수 없는 항목이 포함되어 있습니다.",
      "invalid_ingredients": ["사람", "플라스틱"]
    }}

    - 각 요리의 난이도를 1~5 사이 숫자로 표기하세요.
      - 1: 간단하고 재료가 적으며 조리 시간이 짧은 요리
      - 5: 많은 재료, 전문적인 조리도구 또는 긴 조리시간이 필요한 고급 요리

    - 응답은 반드시 **JSON 형식 본문만 출력**하세요.  
      JSON 외의 설명 텍스트, 해설, 코드 블록(```json 등)은 포함하지 마세요.

    ✅ 올바른 출력 예시:
    {{
      "recipes": [
        {{
          "food": "참치 토마토 오픈 샌드위치",
          "use_ingredients": ["빵", "참치", "토마토", "참기름"],
          "difficulty": 2
        }},
        {{
          "food": "오징어 볶음 스파게티",
          "use_ingredients": ["밀가루", "오징어", "간장", "토마토"],
          "difficulty": 4
        }}
      ]
    }}

    ❌ 잘못된 출력 예시(이런 출력은 절대 하지 마세요):
    - 금지된 재료가 포함된 요리를 출력:
    {{
      "recipes": [
        {{
          "food": "플라스틱 국",
          "use_ingredients": ["플라스틱", "물"],
          "difficulty": 5
        }}
      ]
    }}

    - JSON 외의 텍스트 설명 포함:
    아래의 레시피를 참고하세요!
    {{
      "recipes": [...]
    }}

    - 코드 블록을 사용한 경우:
    ```json
    {{
      "recipes": [...]
    }}
    """

    @staticmethod
    def build_recipe_prompt(food: str, ingredients: list) -> str:
        return f"""
        당신은 전문 요리사 AI입니다. 사용자가 요청한 음식의 상세 조리법을 제공합니다.

        요청된 음식: {food}  
        사용 가능한 재료: {json.dumps(ingredients, ensure_ascii=False)}

        필수 조건:
        - 반드시 요청된 음식과 제공된 재료만 사용하세요.
        - 재료의 양을 구체적으로 명시하세요. (예: 100g, 1컵 등)
        - 레시피는 최소 450자 이상, 단계별로 구성하세요.
        - 식재료로 사용할 수 없는 항목(예: 사람, 동물, 똥, 오줌, 플라스틱, 돌, 금속, 종이, 유리 등 비식용 물질)이 포함되어 있다면, 
        요리 추천은 절대 하지 말고 다음의 에러 메시지만 정확히 JSON 형태로 출력하세요:
        {{"error": "식재료로 사용할 수 없는 항목이 포함되어 있습니다."}}
        - 추가 팁이 있다면 포함하세요.
        - 응답은 반드시 JSON 형식 본문만 출력하세요. JSON 외의 설명이나 텍스트, 코드 블록(```json 등)은 절대 포함하지 마세요.

        참고 예시:
        ```json
        {{
          "food": "계란 오믈렛",
          "use_ingredients": [
            {{"name": "계란", "amount": "2개"}},
            {{"name": "양파", "amount": "50g"}},
            {{"name": "치즈", "amount": "30g"}},
            {{"name": "우유", "amount": "50ml"}}
          ],
          "steps": [
            "양파를 잘게 썰어 준비합니다.",
            "볼에 계란을 깨서 우유, 치즈와 섞어줍니다.",
            "팬에 양파를 볶은 후 혼합물을 붓고 천천히 익힙니다.",
            "반으로 접고 완성합니다."
          ],
          "tip": "우유를 넣으면 더 부드럽게 익습니다."
        }}
        ```
        """

    @staticmethod
    def build_quick_prompt(chat: str) -> str:
        return f"""
        당신은 요리 전문가 AI입니다. 사용자가 입력한 재료를 활용해 만들 수 있는 1가지 요리를 추천하세요.

        입력된 재료: "{chat}"

        필수 조건:
        - 입력된 재료만 사용하세요.
        - 식재료로 사용할 수 없는 항목(예: 사람, 동물, 똥, 오줌, 플라스틱, 돌, 금속, 종이, 유리 등 비식용 물질)이 포함되어 있다면, 
        요리 추천은 절대 하지 말고 다음의 에러 메시지만 정확히 JSON 형태로 출력하세요:
        {{"error": "식재료로 사용할 수 없는 항목이 포함되어 있습니다."}}
        - 조리법은 최소 350자 이상, 단계별로 구성하세요.
        - 재료의 사용량, 조리 팁을 포함하세요.
        - 응답은 반드시 JSON 형식 본문만 출력하세요. JSON 외의 설명이나 텍스트, 코드 블록(```json 등)은 절대 포함하지 마세요.

        잘못된 입력일 경우:
        {{"error": "정확한 음식명을 입력해 주세요."}}

        참고 예시:
        {{
          "food": "치즈 오믈렛",
          "use_ingredients": [
            {{"name": "계란", "amount": "2개"}},
            {{"name": "치즈", "amount": "30g"}},
            {{"name": "우유", "amount": "50ml"}}
          ],
          "steps": ["..."],
          "tip": "약불에서 천천히 익히세요."
        }}
        """

    @staticmethod
    def build_search_prompt(chat: str) -> str:
        return f"""
        당신은 요리 전문가 AI입니다. 사용자가 입력한 음식명에 대한 정확한 레시피를 제공합니다.

        입력된 음식명: "{chat}"

        필수 조건:
        - 해당 음식만 다루고, 추천이나 설명은 생략하세요.
        - 식재료로 사용할 수 없는 항목(예: 사람, 동물, 똥, 오줌, 플라스틱, 돌, 금속, 종이, 유리 등 비식용 물질)이 포함되어 있다면, 
        요리 추천은 절대 하지 말고 다음의 에러 메시지만 정확히 JSON 형태로 출력하세요:
        {{"error": "식재료로 사용할 수 없는 항목이 포함되어 있습니다."}}
        - 재료와 양, 조리법, 팁을 포함한 JSON을 제공하세요.
        - 최소 300자 이상의 단계별 설명 포함.
        - 응답은 반드시 JSON 형식 본문만 출력하세요. JSON 외의 설명이나 텍스트, 코드 블록(```json 등)은 절대 포함하지 마세요.

        잘못된 입력일 경우:
        {{"error": "정확한 음식명을 입력해 주세요."}}

        참고 예시:
        {{
          "food": "된장찌개",
          "use_ingredients": [...],
          "steps": [...],
          "tip": "육수에 마늘을 추가하세요."
        }}
        """
